#include <stdio.h>
#include <stdlib.h>

int main(void){
    printf("%d\n",20+13);
    printf("%d\n",20-13);
    printf("%d\n",20*13);
    printf("%d\n",20/13);
    printf("%d\n\n",20%13);

    printf("%f\n",20.1+13);
    printf("%f\n",20.2-13);
    printf("%f\n",20.3*13);
    printf("%f\n",20.4/13);

    return 0;
}
