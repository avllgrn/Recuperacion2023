#include <stdio.h>
#include <stdlib.h>

int main(void){
    printf("%d + %d = %d\n",2,3,2+3);
    printf("%d - %d = %d\n",2,3,2-3);
    printf("%d * %d = %d\n",2,3,2*3);
    printf("%d / %d = %d\n",2,3,2/3);
    printf("%d %% %d = %d\n\n",2,3,2%3);

    printf("%f + %f = %f\n",2.5,3.5,2.5+3.5);
    printf("%f - %f = %f\n",2.5,3.5,2.5-3.5);
    printf("%f * %f = %f\n",2.5,3.5,2.5*3.5);
    printf("%f / %f = %f\n\n",2.5,3.5,2.5/3.5);

    return 0;
}
